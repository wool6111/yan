package sist.com.dto;

public class ReviewBean {
	private int rviewno;
	private String rtitle;
	private int ruser;
	private String rpass;
	private String rcontents;
	private int score;
	private String rregdate;
	private int rentno;
	
	public int getRviewno() {
		return rviewno;
	}
	public void setRviewno(int rviewno) {
		this.rviewno = rviewno;
	}
	public String getRtitle() {
		return rtitle;
	}
	public void setRtitle(String rtitle) {
		this.rtitle = rtitle;
	}
	public int getRuser() {
		return ruser;
	}
	public void setRuser(int ruser) {
		this.ruser = ruser;
	}
	public String getRpass() {
		return rpass;
	}
	public void setRpass(String rpass) {
		this.rpass = rpass;
	}
	public String getRcontents() {
		return rcontents;
	}
	public void setRcontents(String rcontents) {
		this.rcontents = rcontents;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public String getRregdate() {
		return rregdate;
	}
	public void setRregdate(String rregdate) {
		this.rregdate = rregdate;
	}
	public int getRentno() {
		return rentno;
	}
	public void setRentno(int rentno) {
		this.rentno = rentno;
	}
	
	

}
